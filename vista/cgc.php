                            <p>Es una amplia categoria de procedimientos, aplicaciones y tecnologias para recoletar, guardar, analizar, compartir y proveer acceso a los datos con el propósito de ayudar a los usuarios/empresas a tomar mejores decisiones. Gartner</p>
                            <p>BI es una estrategia de negocios que se basa en: Análisis de datos y fácil acceso; cumpliiento de Normas; orientación al usuario final; apoyo en la toma de decisiones. </p>
                            <p>BI no es un proyecto de TI.</p>
                            <p>El valor de BI: Los negocios son una guerra! como en cualquier guerra, sobrevivir depende de la capacidad para actuar rápidamente en un ambiente cambiante. BI sera como un comando de control central para rastrear variables como el desarrollo operacional, las condiciones del mercado y el desarrollo de los competidores. Todos ellos en tiempo real.</p>
                            <p><strong>Componentes:</strong></p>
                            <ul>
                                <li>Fuentes de datos transaccionales: BD, OLTP. </li>
                                <li>ETL: para extracción, transformación y carga de DW.</li>
                                <li>DataWarehouse: almacen de datos desnormalizados. </li>
                                <ul>
                                    <li>OLAP(*): Solución de cálculo y consulta a estructuras multidimensionales.</li>
                                    <li>Dataminig(*): Solución que permite análisis predictivos. </li>
                                </ul>
                                <li>Servidor de BI: motor de calculo y generación de reportes ad-hoc. </li>
                                <li>Herramientas para el usuario final, capa de presentación de reportes generales.</li>
